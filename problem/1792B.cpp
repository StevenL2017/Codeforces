void solve() {
    int a1, a2, a3, a4;
    cin >> a1 >> a2 >> a3 >> a4;

    if (a1 == 0) {
        cout << 1 << endl;
        return;
    }

    int ans = a1;
    auto d = min(a2, a3);
    ans += d * 2;
    a2 -= d;
    a3 -= d;
    ans += min(a1 + 1, max(a2, a3) + a4);

    cout << ans << endl;
}

