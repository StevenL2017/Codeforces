void solve() {
    int n;
    cin >> n;

    vi digits;
    while (n > 0) {
        digits.push_back(n % 10);
        n /= 10;
    }

    reverse(digits.begin(), digits.end());
    int m = digits.size();
    int x = 0, y = 0;
    for (int i = 0; i < m; i += 2) {
        x *= 10;
        x += digits[i];
    }
    for (int i = 1; i < m; i += 2) {
        y *= 10;
        y += digits[i];
    }
    cout << (x + 1) * (y + 1) - 2 << endl;
}

