void solve() {
    int a, b, c;
    cin >> a >> b >> c;

    for (auto x: {a, a / 2, a / 3, a / 4}) {
        if (x == 0) {
            continue;
        }
        int ok = 1, cnt = 0;
        for (auto y: {a, b, c}) {
            if (y % x != 0) {
                ok = 0;
                break;
            }
            cnt += y / x - 1;
        }
        if (ok && cnt <= 3) {
            cout << "YES\n";
            return;
        }
    }
    cout << "NO\n";
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t = 1;
    cin >> t;
    while (t--) solve();
}