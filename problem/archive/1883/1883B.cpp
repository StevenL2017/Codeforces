template <class T> void out(const vector<T>& a) { for (int i = 0; i < ssz(a); i++) cout << a[i] << " \n"[i + 1 == ssz(a)]; }

void solve() {
    int n, k;
    cin >> n >> k;
    string s;
    cin >> s;

    vi cnt(26);
    for (auto c: s) {
        cnt[c - 'a']++;
    }

    int odd = 0;
    for (auto x: cnt) {
        if (x & 1) {
            odd++;
        }
    }

    if (odd > 1) {
        k -= odd - 1;
    }

    if (k < 0) {
        cout << "NO\n";
    } else {
        cout << "YES\n";
    }
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t = 1;
    cin >> t;
    while (t--) solve();
}