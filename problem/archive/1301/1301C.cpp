void solve() {
    int n, m; cin >> n >> m;

    int cnt0 = n - m, d = m + 1;
    int a = cnt0 / d, b = cnt0 % d;

    cout << n * 1ll * (n + 1) / 2 - ((a + 1) * 1ll * (a + 1 + 1) * b / 2 + a * 1ll * (a + 1) * (d - b) / 2) << endl;
}

