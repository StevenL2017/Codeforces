void solve() {
    int n, m;
    cin >> n >> m;

    ll mx = (n - m) * 1ll * (n - m + 1) / 2;
    int x = (n - m) / m;
    ll mn = x * 1ll * (x + 1) / 2 * m + (x + 1) * 1ll * ((n - m) % m);

    cout << mn << " " << mx << endl;
}

