template <class T> void out(const vector<T>& a) { for (int i = 0; i < ssz(a); i++) cout << a[i] << " \n"[i + 1 == ssz(a)]; }

void solve() {
    int n; cin >> n;
    vector<pair<int, int>> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i].first >> a[i].second;
    }

    sort(a.begin(), a.end());

    int ans = 0, pre = -1e9;
    for (int i = 0; i < n; i++) {
        if (a[i].first - pre > a[i].second) {
            ans++;
            pre = a[i].first;
        }
        else if (i == n - 1 || a[i + 1].first - a[i].first > a[i].second) {
            ans++;
            pre = a[i].first + a[i].second;
        }
        else {
            pre = a[i].first;
        }
    }
    cout << ans << endl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t = 1;
    // cin >> t;
    while (t--) solve();
}