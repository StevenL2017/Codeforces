#define ssz(a) (int)((a).size())

#define endl '\n'
template <class T> void in(vector<T>& a) { for (int i = 0; i < ssz(a); i++) cin >> a[i]; }
template <class T> void out(const vector<T>& a) { for (int i = 0; i < ssz(a); i++) cout << a[i] << " \n"[i + 1 == ssz(a)]; }

void solve() {
    int k, x, a;
    cin >> k >> x >> a;

    ll pre = 0;
    for (int i = 0; i <= x; i++) {
        ll cur = pre / (k - 1) + 1;
        if (a < cur) {
            cout << "NO\n";
            return;
        }
        a -= cur;
        pre += cur;
    }
    cout << "YES\n";
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t = 1;
    cin >> t;
    while (t--) solve();
}