const int MOD = 998244353;

int add(int x, int y) {
    x += y;
    while (x >= MOD) x -= MOD;
    while (x < 0) x += MOD;
    return x;
}

int sub(int x, int y) {
    return add(x, -y);
}

int mul(int x, int y) {
    return (x * 1ll * y) % MOD;
}

void solve() {
    int n;
    cin >> n;
    vector<vi> e(n);
    for (int i = 0; i < n - 1; i++) {
        int u, v;
        cin >> u >> v;
        u--; v--;
        e[u].push_back(v);
        e[v].push_back(u);
    }

    int ans = 1;
    function<int(int, int)> dfs = [&] (int u, int p) -> int {
        int cur = 1;
        for (auto v: e[u]) {
            if (v == p) {
                continue;
            }
            int nxt = dfs(v, u);
            cur = mul(cur, add(nxt, 1));
        }
        ans = add(ans, cur);
        return cur;
    };
    dfs(0, -1);
    cout << ans << endl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t = 1;
    cin >> t;
    while (t--) solve();
}