void solve() {
    int x, n;
    cin >> x >> n;

    int ans = 1, z = 1;
    while (z * z <= x) {
        if (x % z == 0 && x / z >= n) {
            ans = max(ans, z);
        }
        if (x % z == 0 && x / (x / z) >= n) {
            ans = max(ans, x / z);
        }
        z++;
    }
    cout << ans << endl;
}

