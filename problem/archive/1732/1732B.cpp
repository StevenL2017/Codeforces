void solve() {
    int n; cin >> n;
    string s; cin >> s;

    int ans = 0, i = 0;
    for (int j = 0; j < n; j++) {
        if (s[j] == '1') {
            i = j;
            break;
        }
    }
    for (int j = i; j < n - 1; j++) {
        if (s[j] != s[j + 1]) {
            ans++;
        }
    }

    cout << ans << endl;
}

