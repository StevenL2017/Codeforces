void solve() {
    int n;
    cin >> n;

    int m = (int)(sqrt(2 * n));
    while ((m + 1) * m <= 2 * n) {
        m++;
    }
    int r = n - m * (m - 1) / 2;

    string ans = "133";
    while (r) {
        ans += '7';
        r--;
    }
    m -= 2;
    while (m) {
        ans += '3';
        m--;
    }
    ans += '7';

    cout << ans << endl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t = 1;
    cin >> t;
    while (t--) solve();
}