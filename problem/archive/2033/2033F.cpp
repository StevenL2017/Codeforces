const int MOD = 1e9 + 7;

void solve() {
    ll n, k;
    cin >> n >> k;

    int a = 0, b = 1 % k, cnt = 0;
    do {
        cnt++;
        int c = (a + b) % k;
        a = b;
        b = c;
    } while (a);
    cout << cnt * (n % MOD) % MOD << endl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t = 1;
    cin >> t;
    while (t--) solve();
}