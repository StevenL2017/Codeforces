void solve() {
    int n; string s;
    cin >> n >> s;

    int ans = 0;
    for (int i = 0, j = 0; i < n; ) {
        while (j < n && s[j] == s[i]) {
            j++;
        }
        if ((j - i) & 1) {
            ans++;
            i = --j;
            if (s[i] == '0') s[i] = '1';
            else s[i] = '0';
        } else {
            i = j;
        }
    }

    cout << ans << endl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t = 1;
    cin >> t;
    while (t--) solve();
}