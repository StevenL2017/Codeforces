void solve() {
    int n; cin >> n;
    vector<ll> a(n); in(a);

    auto mx = *max_element(a.begin(), a.end());
    if (n > 3) {
        cout << n * mx << endl;
    }
    else if (n == 3) {
        cout << max({3 * a[0], 3 * a[2], 3 * abs(a[0] - a[1]), 3 * abs(a[2] - a[1]), a[0] + a[1] + a[2]}) << endl;
    }
    else {
        cout << max(a[0] + a[1], 2 * abs(a[0] - a[1])) << endl;
    }
}

