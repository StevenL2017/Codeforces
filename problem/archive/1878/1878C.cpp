void solve() {
    int n, k;
    cin >> n >> k;
    ll x;
    cin >> x;

    ll mn = (1 + k) * 1ll * k / 2;
    ll mx = (n + n - k + 1) * 1ll * k / 2;
    if (mn <= x && x <= mx) cout << "YES\n";
    else cout << "NO\n";
}

