void solve() {
    int n;
    cin >> n;

    string ans;
    ll a = 0, g = 0;
    while (n--) {
        int x, y;
        cin >> x >> y;
        if (abs(x + a - g) <= 500) {
            a += x;
            ans += 'A';
        } else {
            g += y;
            ans += 'G';
        }
    }

    cout << ans << endl;
}

