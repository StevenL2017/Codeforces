class UnionFind {
private:
    int N;
    int *f;
    int *sz;
    int count;

public:
    UnionFind(int n) :
        N(n), count(n) {
        f = new int[N]();
        sz = new int[N]();
        for (int i = 0; i < N; i++) {
            f[i] = i;
            sz[i] = 1;
        }
    }
    
    ~UnionFind() {
        delete[] f;
        delete[] sz;
    }

    int find(int x) {
        if (x != f[x]) f[x] = find(f[x]);
        return f[x];
    }

    bool merge(int x, int y) {
        int x_ = find(x), y_ = find(y);
        if (x_ == y_) return false;
        if (sz[x_] < sz[y_]) {
            int temp = y_; y_ = x_; x_ = temp;
        }
        f[y_] = x_;
        sz[x_] += sz[y_];
        count--;
        return true;
    }

    bool same(int x, int y) {
        return find(x) == find(y);
    }
};

void solve() {
    int n, k; cin >> n >> k;
    string s; cin >> s;

    UnionFind uf(n);
    rep(i, n) {
        uf.merge(i, n - 1 - i);
        if (i + k < n) uf.merge(i, i + k);
    }

    map<int, map<char, int>> cnt;
    rep(i, n) {
        cnt[uf.find(i)][s[i]]++;
    }

    int ans = 0;
    for (auto& [k, v]: cnt) {
        int tot = 0, mx = 0;
        for (auto& [_, c]: v) {
            tot += c;
            mx = max(mx, c);
        }
        ans += tot - mx;
    }

    cout << ans << endl;
}

