void solve() {
    int n; string s1, s2;
    cin >> n >> s1 >> s2;

    map<pair<char, char>, int> cnt;
    rep(i, n) {
        auto f = min(s1[i], s2[n - 1 - i]);
        auto s = max(s1[i], s2[n - 1 - i]);
        cnt[{f, s}]++;
    }

    int odd = 0;
    for (auto& [k, v]: cnt) {
        if (v & 1) {
            if (k.first != k.second) {
                cout << "NO\n";
                return;
            }
            odd++;
            if (odd > 1) {
                cout << "NO\n";
                return;
            }
        }
    }
    cout << "YES\n";
}

