using namespace std;

#define ll long long
#define vi vector<int>
#define ssz(a) (int)((a).size())

#define endl '\n'
template <class T> void in(vector<T>& a) { for (int i = 0; i < ssz(a); i++) cin >> a[i]; }
template <class T> void out(const vector<T>& a) { for (int i = 0; i < ssz(a); i++) cout << a[i] << " \n"[i + 1 == ssz(a)]; }

void solve() {
    ll n, m;
    cin >> n >> m;
    vector<ll> a(n);
    in(a);

    sort(a.begin(), a.end());

    ll ans = 0, s = 0;
    int left = 0;
    for (int right = 0; right < n; right++) {
        s += a[right];
        while (left <= right && (s > m || a[right] - a[left] > 1)) {
            s -= a[left++];
        }
        ans = max(ans, s);
    }

    cout << ans << endl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t = 1;
    cin >> t;
    while (t--) solve();
}