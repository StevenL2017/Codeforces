using namespace std;

#define ll long long
#define vi vector<int>
#define ssz(a) (int)((a).size())

#define endl '\n'
template <class T> void in(vector<T>& a) { for (int i = 0; i < ssz(a); i++) cin >> a[i]; }
template <class T> void out(const vector<T>& a) { for (int i = 0; i < ssz(a); i++) cout << a[i] << " \n"[i + 1 == ssz(a)]; }

void solve() {
    int n; cin >> n;
    vi a(n); in(a);

    ll ans = 0, i = 0;
    while (i < n && a[i] == 0) {
        i++;
    }
    if (i < n) ans = 1;
    while (i < n) {
        auto i0 = i;
        i++;
        while (i < n && a[i] == 0) {
            i++;
        }
        if (i == n) break;
        ans *= (i - i0);
    }
    cout << ans << endl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t = 1;
    // cin >> t;
    while (t--) solve();
}