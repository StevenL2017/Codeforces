void solve() {
    int n; cin >> n;
    vector<ll> a(n); in(a);

    for (auto x : a) {
        int s = sqrt(x);
        int y = 2;
        bool ok = true;
        while (y * y <= s) {
            if (s % y == 0) {
                ok = false;
                break;
            }
            y++;
        }
        if (s > 1 && s * 1ll * s == x && ok) {
            cout << "YES\n";
        } else {
            cout << "NO\n";
        }
    }
}

