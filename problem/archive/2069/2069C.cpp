#define ll long long
#define vi vector<int>
#define ssz(a) (int)((a).size())

#define endl '\n'
template <class T> void in(vector<T>& a) { for (int i = 0; i < ssz(a); i++) cin >> a[i]; }
template <class T> void out(const vector<T>& a) { for (int i = 0; i < ssz(a); i++) cout << a[i] << " \n"[i + 1 == ssz(a)]; }

const int MOD = 998244353;

int add(int x, int y) {
    x += y;
    while (x >= MOD) x -= MOD;
    while (x < 0) x += MOD;
    return x;
}

int sub(int x, int y) {
    return add(x, -y);
}

int mul(int x, int y) {
    return (x * 1ll * y) % MOD;
}

void solve() {
    int n;
    cin >> n;
    vi a(n);
    in(a);

    int ans = 0;
    vi cnt(2);
    for (int i = 0; i < n; i++) {
        if (a[i] == 1) {
            cnt[0]++;
        } else if (a[i] == 2) {
            cnt[1] = mul(cnt[1], 2);
            cnt[1] = add(cnt[1], cnt[0]);
        } else {
            ans = add(ans, cnt[1]);
        }
    }
    cout << ans << endl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t = 1;
    cin >> t;
    while (t--) solve();
}