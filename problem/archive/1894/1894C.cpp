#define ll long long
#define vi vector<int>
#define ssz(a) (int)((a).size())

#define endl '\n'
template <class T> void in(vector<T>& a) { for (int i = 0; i < ssz(a); i++) cin >> a[i]; }
template <class T> void out(const vector<T>& a) { for (int i = 0; i < ssz(a); i++) cout << a[i] << " \n"[i + 1 == ssz(a)]; }

void solve() {
    int n, k;
    cin >> n >> k;
    vi b(n); in(b);

    int c = 0, i = n - 1;
    vi vis(n, 0);
    while (!vis[i]) {
        vis[i] = 1;
        if (b[i] > n) {
            cout << "No\n";
            return;
        }
        c++;
        if (c == k) {
            cout << "Yes\n";
            return;
        }
        i = (i - b[i] + n) % n;
    }
    cout << "Yes\n";
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t = 1;
    cin >> t;
    while (t--) solve();
}